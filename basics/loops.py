# Loops in Python
# A loop allows us to repeat a block of code multiple times.

#For Loop → iterate over a sequence.
# Example: For loop
for i in range(5):
    print("Iteration:", i)

#While Loop → repeat until a condition becomes false.
# Example: While loop
count = 0
while count < 3:
    print("Count is:", count)
    count += 1




